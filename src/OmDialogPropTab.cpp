///
///  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -
///
void OmDialogPropTab::paramCheck(unsigned i)
{
  _chParam[i] = true;

  if(this->_parent != nullptr)
    static_cast<OmDialogProp*>(this->_parent)->checkChanges();
}


///
///  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -
///
void OmDialogPropTab::paramReset(unsigned i)
{
  this->_chParam[i] = false;

  if(this->_parent != nullptr)
    static_cast<OmDialogProp*>(this->_parent)->checkChanges();
}


///
///  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -
///
void OmDialogPropTab::_paramResetAll()
{
  for(size_t i = 0; i < OMDIALOGPROP_MAX_PARAM; ++i)
    this->_chParam[i] = false;
}


